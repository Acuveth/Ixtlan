/** Routes each role can access */
const roleRoutes: Record<UserRole, string[]> = {
  admin: ['/', '/plans', '/plan-generator', '/observation', '/pipeline', '/budget', '/admin', '/notifications', '/locations'],
  planner: ['/', '/plans', '/plan-generator', '/observation', '/pipeline', '/budget', '/notifications', '/locations'],
  field_worker: ['/', '/planned-work', '/observation', '/pipeline', '/notifications', '/locations'],
  lab_worker: ['/', '/lab-queue', '/observation', '/pipeline', '/notifications', '/locations'],
  analyst: ['/', '/plans', '/observation', '/pipeline', '/budget', '/notifications', '/locations'],
};

export function getNavItemsForRole(role: UserRole): NavItem[] {
  const allowed = roleRoutes[role];
  return allNavItems.filter(item => allowed.includes(item.to));
}

export function canAccessRoute(role: UserRole, path: string): boolean {
  if (roleRoutes[role].includes(path)) return true;
  // Support dynamic routes like /locations/:id
  if (path.startsWith('/locations/') && roleRoutes[role].includes('/locations')) return true;
  return false;
}

export function getDefaultRoute(role: UserRole): string {
  return '/';
}

export function getRoleLabel(role: UserRole): string {
  const labels: Record<UserRole, string> = {
    admin: 'Admin',
    planner: 'Planner',
    field_worker: 'Field Worker',
    lab_worker: 'Lab Worker',
    analyst: 'Analyst',
  };
  return labels[role];
}
