/* ---- Action Button Component ---- */

function ActionButton({
  action,
  isOpen,
  disabled,
  onClick,
  onOptionClick,
}: {
  action: ChatAction;
  isOpen: boolean;
  disabled: boolean;
  onClick: () => void;
  onOptionClick: (prompt: string) => void;
}) {
  if (action.type === 'dropdown' && action.options) {
    return (
      <div className="chat-action-dropdown">
        <button className="chat-action-btn" onClick={(e) => { e.stopPropagation(); onClick(); }} disabled={disabled}>
          <svg viewBox="0 0 24 24" xmlns="http://www.w3.org/2000/svg">
            <path d={actionIcons[action.icon]} fill="currentColor" />
          </svg>
          {action.label}
          <svg className={`chat-action-chevron${isOpen ? ' open' : ''}`} viewBox="0 0 24 24" xmlns="http://www.w3.org/2000/svg">
            <path d="M7 10l5 5 5-5z" fill="currentColor" />
          </svg>
        </button>
        {isOpen && (
          <div className="chat-dropdown-menu">
            {action.options.map((opt) => (
              <button
                key={opt.value}
                className="chat-dropdown-item"
                onClick={() => onOptionClick(opt.prompt)}
              >
                {opt.label}
              </button>
            ))}
          </div>
        )}
      </div>
    );
  }

  return (
    <button className="chat-action-btn" onClick={onClick} disabled={disabled}>
      <svg viewBox="0 0 24 24" xmlns="http://www.w3.org/2000/svg">
        <path d={actionIcons[action.icon]} fill="currentColor" />
      </svg>
      {action.label}
    </button>
  );
}
