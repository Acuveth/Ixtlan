/* ---- Reliability Badge ---- */

function ReliabilityBadge({ score }: { score?: number }) {
  if (score === undefined || score === null) return null;
  const color = score >= 90 ? '#639922' : score >= 70 ? '#378ADD' : score >= 50 ? '#EF9F27' : '#E24B4A';
  const label = score >= 90 ? 'High' : score >= 70 ? 'Good' : score >= 50 ? 'Fair' : 'Low';
  return (
    <span className="ai-reliability" style={{ color, borderColor: color + '44' }} title={`AI confidence: ${score}%`}>
      {score}% {label}
    </span>
  );
}

